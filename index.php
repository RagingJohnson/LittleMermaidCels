                            </p>
                        </div>
                    </div>
                </div>
                <div class="accordion-item">
                    <h2 class="accordion-header" id="flush-headingMaster">
                        <button class="accordion-button collapsed lead" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseMaster" aria-expanded="false" aria-controls="flush-collapseMaster">
                            What is a Master Background?
                        </button>
                    </h2>
                    <div id="flush-collapseMaster" class="accordion-collapse collapse" aria-labelledby="flush-headingMaster">
                        <div class="accordion-body">
                            <p class="lead">
                                A Master Background is the original background painting (often watercolour, but not always) that was placed under the camera, behind the Cels, and was photographed as part of the frame of the film. Since many Cels exist per each background, most Cel Setups include a photocopy of the original background (or similar) for presentation purposes.
                            </p>
                            <p class="lead">
                                Some Cels, largely those from the Sotheby’s 1990 auction, did include their original Master Backgrounds when they were sold. If they also happen to be a Key Setup, these are called a Key-Master Setup, which are the most rare and valuable type of Cel Setups, as all featured components of what created the final frame of the film are present in a single piece.
                            </p>
                        </div>
                    </div>
                </div>
                <div class="accordion-item">
                    <h2 class="accordion-header" id="flush-headingFraming">
                        <button class="accordion-button collapsed lead" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseFraming" aria-expanded="false" aria-controls="flush-collapseFraming">
                            What is the Significance of Framing?
                        </button>
                    </h2>
                    <div id="flush-collapseFraming" class="accordion-collapse collapse" aria-labelledby="flush-headingFraming">
                        <div class="accordion-body">
                            <p class="lead">
                                The framing status of a Cel is significant for two reasons.
                            </p>
                            <p class="lead">
                                The first is that a framed Cel is implicitly better protected against damage. Since the Cel has already been framed, the purchaser will not have to go to the additional trouble and expense of having it framed.
                            </p>
