                <div class="accordion-item">
                    <h2 class="accordion-header" id="flush-headingCel">
                        <button class="accordion-button collapsed lead" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseCel" aria-expanded="false" aria-controls="flush-collapseCel">
                            What is a Cel?
                        </button>
                    </h2>
                    <div id="flush-collapseCel" class="accordion-collapse collapse" aria-labelledby="flush-headingCel">
                        <div class="accordion-body">
                            <p class="lead">
                                A celluloid, or “Cel”, is a piece of clear sheet plastic that is used in traditional animation. After an exhausting production process, the line art of the final image is copied onto the front of the Cel. Paint is then applied to the reverse side, so that the lines show on top of the colour applied underneath.
                            </p>
                            <p class="lead">
                                The Cels are then placed on top of a Master Background and photographed in succession to create the frames of the film. Traditional animation runs at a frame rate of 24 frames per second (FPS).
                            </p>
                            <p class="lead">
                                In early animation production, Cels were made from highly volatile cellulose nitrate. With the introduction of cellulose acetate, or “safety film” filmstock, animation Cels eventually transitioned to the new material. Cels from The Little Mermaid (1989) were made with cellulose acetate.
                            </p>
                            <p class="lead">
                                While remarkably less combustible than nitrate, acetate is subject to “vinegar syndrome”, where the material breaks down into separate chemical components, one of which being acetic acid, which gives vinegar its distinct smell. This delayed-onset degradation of the material has wreaked havoc on traditional film collections as the decades passed, and also places animation Cels in jeopardy. 
                            </p>
                            <p class="lead">
                                Following the guidelines for filmstock conservation, Cels should be kept in cool, dry environments, out of direct sunlight and other intense lighting sources. Additionally, Cels should be framed to museum conservation standards to further stabilise their environment and ensure maximum longevity of the material.
                            </p>
                            <p class="lead">
                                The term “Cel” is not to be confused with the similarly spelled “Cell”, which refers to an individual frame on a strip of filmstock.
                            </p>
                        </div>
                    </div>
                </div>
                <div class="accordion-item">
                    <h2 class="accordion-header" id="flush-headingLM">
                        <button class="accordion-button collapsed lead" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseLM" aria-expanded="false" aria-controls="flush-collapseLM">
                            What is a Little Mermaid Cel?
                        </button>
                    </h2>
                    <div id="flush-collapseLM" class="accordion-collapse collapse" aria-labelledby="flush-headingLM">
                        <div class="accordion-body">
                            <p class="lead">
                                The Little Mermaid (1989) <a href="#" data-toggle="tooltip" title="Ronald Varney, The Art of The Little Mermaid (New York: Sotheby's, August 1990), viii-x">used no less than 150,000 Cels</a> to create a runtime of 01:23:00. It is estimated that only 10% of Cels survive the animation process. After production of the film was completed, many of the surviving Cels were packaged up by the Disney Company into “Cel Setups” and sold to the private market through an official auction (Sotheby’s Art of The Little Mermaid, 15th December 1990, New York), as well as through Disney’s own Disney Store retail sites and affilitated galleries through the Disney Art Program.
                            </p>
                            <p class="lead">
                                Some Cels were given to employees as part of their employment agreement, but some Cels were taken from the studios without authorisation. In the case of the latter, such Cels are illegitimate collector’s items, as they are stolen property.
                            </p>
                            <p class="lead">
                                The Little Mermaid was the last Disney feature film to use Cel-based animation. The next feature, The Rescuers Down Under (1990), used the Computer Animation Production Systems (CAPS), which removed Cels from the production process.
                            </p>
                        </div>
                    </div>
                </div>
                <div class="accordion-item">
                    <h2 class="accordion-header" id="flush-headingCover">
                        <button class="accordion-button collapsed lead" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseCover" aria-expanded="false" aria-controls="flush-collapseCover">
                            What This Site Doesn’t Cover
                        </button>
                    </h2>
                    <div id="flush-collapseCover" class="accordion-collapse collapse" aria-labelledby="flush-headingCover">
                        <div class="accordion-body">
                            <p class="lead">
                                This site focuses exclusively on Production Cels (i.e. Cels that went under the camera to create the frames of the film).
                            </p>
                            <p class="lead">
                                This site will not concern itself with pre-/post-/production materials, including: concept art, storyboard art, colour model Cels, character maquettes (model statues), character model sheets, preliminary backgrounds or production background paintings that were not actually used in the final film. I will include productions Cels that were made but ultimately not used in the film (there are a couple), provided I am confident of their authenticity.
                            </p>
                            <p class="lead">
                                Additionally, I’m not at all concerned with post-production “Collector’s Edition” Cels, like Sericels and Hand-Painted Limited-Edition Cels, nor artwork produced for derivative works, including picture books, marketing materials, and subsequent works of animation (pre/sequel films, television shows, etc).
                            </p>
                        </div>
                    </div>
                </div>
                <div class="accordion-item">
                    <h2 class="accordion-header" id="flush-headingConditions">
                        <button class="accordion-button collapsed lead" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseConditions" aria-expanded="false" aria-controls="flush-collapseConditions">
                            Conditions of Cels
                        </button>
                    </h2>
                    <div id="flush-collapseConditions" class="accordion-collapse collapse" aria-labelledby="flush-headingConditions">
                        <div class="accordion-body">
                            <p class="lead">
                                The condition of a Cel refers to both its physical integrity (damage/restoration history/framing conditions), as well as the hallmarks of its authenticity and legitimacy.
                            </p>
                            <p class="lead">
                                Although some Cels have sustained damage since production, many Cels remain in good condition.
                            </p>
                            <p class="lead">
                                The hallmarks that are used to authenticate a Cel include a Seal of Authenticity (or seal), a Certificate of Authenticity (CoA), its ability to be located in the film, the peg-hole markings, and the piece’s sales history.
                            </p>
                        </div>
                    </div>
                </div>
