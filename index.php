                    <div id="flush-collapseSeal" class="accordion-collapse collapse" aria-labelledby="flush-headingSeal">
                        <div class="accordion-body">
                            <p class="lead">
                                A seal is a stamped marking that has been added to a Cel setup post-production by the Disney Company during preparation for sale. There are a few variations on the designs across films over the years, but the designs used on Cels from The Little Mermaid are:
