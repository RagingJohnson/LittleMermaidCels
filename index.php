                <div class="accordion-item">
                    <h2 class="accordion-header" id="flush-headingNoSeal">
                        <button class="accordion-button collapsed lead" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseNoSeal" aria-expanded="false" aria-controls="flush-collapseNoSeal">
                            What If a Cel Has a CoA But Not a Seal?
                        </button>
                    </h2>
                    <div id="flush-collapseNoSeal" class="accordion-collapse collapse" aria-labelledby="flush-headingNoSeal">
                        <div class="accordion-body">
                            <p class="lead">
                                As CoA’s are generic in design, and do not sufficiently distinguish the Cel to which they belong, it is possible, and not uncommon, for a CoA to be removed from a Cel that has been legitimately released from the Disney Company, but whose image is less lucrative on the Collector’s Market, and instead be paired with a Cel that is more desirable, but bears no seal.
                            </p>
                            <p class="lead">
                                CoA’s from The Little Mermaid without accompanying artwork are occasionally listed on eBay. The significance of their inclusion in a Cel setup is more an inclination towards completionism, and less about guaranteeing authenticity.
                            </p>
                            <p style="text-align: center;">
                                <button class="btn btn-sm" onclick=" window.open('images/About/COAeBay.jpg','_blank')"><img class="img-fluid img-thumbnail" style="max-height: 200px;" src="images/About/COAeBay.jpg" loading="lazy"></button>
                            </p>
                            <p class="lead">
                                In the case that the Cel does not bear a seal, and does not meet one of the aforementioned conditions of exemption, it is possible the Cel is stolen property, even if accompanied by a legitimate CoA. In terms of collecting, I personally will not buy a Cel unless it bears a seal or fits into a category of exemption. I am not alone in this standard; I deal primarily with a gallery that will not sell Cel’s from the Little Mermaid without seals and, in the wider market, such Cels typically sell for much less than similar pieces that have seals.
                            </p>
                            <p class="lead">
                                Since a seal is stamped onto the Cel in silver ink, it cannot be transferred to another piece, and trumps a CoA when determining authenticity and legitimacy. An exception to this is when two Cels have been paired from radically different scenes and do not have any vague coincidence in the film. In this case, it is possible a less desirable Cel with a seal has been cobbled together with a more desirable Cel that does not have a seal to pass the latter off as legitimate.
                            </p>
                            <p class="lead">
                                When gauging legitimacy, it is important to ask questions of the piece’s history and condition. If the Seller is vague or non-responsive, exercise caution when dealing with them.
                            </p>
                        </div>
                    </div>
                </div>
                <div class="accordion-item">
                    <h2 class="accordion-header" id="flush-headingAutograph">
                        <button class="accordion-button collapsed lead" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseAutograph" aria-expanded="false" aria-controls="flush-collapseAutograph">
                            Autographed Cels
                        </button>
                    </h2>
                    <div id="flush-collapseAutograph" class="accordion-collapse collapse" aria-labelledby="flush-headingAutograph">
                        <div class="accordion-body">
                            <p class="lead">
                                Cels may be autographed by someone who worked on the film. The most common example is Jodi Benson, who provided the voice of Ariel.
                            </p>
                            <p style="text-align: center;">
                                <button class="btn btn-sm" onclick=" window.open('images/About/jodi_benson_cel.jpg','_blank')"><img class="img-fluid img-thumbnail" style="max-height: 160px;" src="images/About/jodi_benson_cel.jpg" loading="lazy"></button>
                                <button class="btn btn-sm" onclick=" window.open('images/About/mark_henn_cel.jpg','_blank')"><img class="img-fluid img-thumbnail" style="max-height: 160px;" src="images/About/mark_henn_cel.jpg" loading="lazy"></button>
                            </p>
                            <p class="lead">
                                It is ill-advised to sign a Cel directly, as the ink adds additional reagents that may destabilise the Cel's chemical composition. In other words, it could lead to the destruction of the Cel over time. It may also lead to direct accidental damage to the piece, as the marker could intersect the fragile ink line on the surface of the Cel and ruin it.
                            </p>
                            <p class="lead">
                                The best practice is to have the contributor sign the mat with which the Cel is framed, leaving the Cel itself untouched.
                            </p>
                            <p style="text-align: center;">
                                <button class="btn btn-sm" onclick=" window.open('images/About/jodi_benson_mat.jpg','_blank')"><img class="img-fluid img-thumbnail" style="max-height: 160px;" src="images/About/jodi_benson_mat.jpg" loading="lazy"></button>
                                <button class="btn btn-sm" onclick=" window.open('images/About/jodi_benson_mat2.jpg','_blank')"><img class="img-fluid img-thumbnail" style="max-height: 160px;" src="images/About/jodi_benson_mat2.jpg" loading="lazy"></button>
                            </p>
                            <p class="lead">
                                Autographs are made at the discretion of the contributor, not upon the authority of the Walt Disney Company. With that in mind, there are Cels with autographs but no seal. An autograph, irrespective of authenticity, does not substitute for a seal, and a stolen item remains as such.
                            </p>
                        </div>
                    </div>
                </div>
                <div class="accordion-item">
                    <h2 class="accordion-header" id="flush-headingKey">
                        <button class="accordion-button collapsed lead" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseKey" aria-expanded="false" aria-controls="flush-collapseKey">
                            What is a Key Setup?
                        </button>
                    </h2>
                    <div id="flush-collapseKey" class="accordion-collapse collapse" aria-labelledby="flush-headingKey">
                        <div class="accordion-body">
                            <p class="lead">
                                A Key Setup is where all Cels in a Cel Setup belong together within the same frame of the film. Since individual layers of Cel Setups can be reorganised, some setups may be missing a Cel, or may be paired with a Cel that does not belong to the same frame. Since some Cels were used across multiple frames ("Held Cels"), it is impossible for all setups to be key. Key Setups are implicitly more valuable, as they are "complete", and represent a 1:1 snapshot of the Cels as they are shown in the film.
                            </p>
                            <p class="lead">
                                Although there are certain types of Cels that were used to create shadows by means of superimposition, these Cels were not typically sold by the Disney Company, as they are solid black, and obstruct the rest of the Cel’s artwork. For the purposes of classification, this site will not take into account the missing shadow Cels when determining whether a setup is a Key Setup.
                            </p>
                            <p style="text-align: center;">
                                <button class="btn btn-sm" onclick=" window.open('images/About/no_shadow_cel.jpg','_blank')"><img class="img-fluid img-thumbnail" style="max-height: 160px;" src="images/About/no_shadow_cel.jpg" loading="lazy"></button>
                                <button class="btn btn-sm" onclick=" window.open('images/About/shadow_full_cel.jpg','_blank')"><img class="img-fluid img-thumbnail" style="max-height: 160px;" src="images/About/shadow_full_cel.jpg" loading="lazy"></button>
