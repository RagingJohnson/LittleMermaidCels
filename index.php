                <div class="accordion-item">
                    <h2 class="accordion-header" id="flush-headingCOA">
                        <button class="accordion-button collapsed lead" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseCOA" aria-expanded="false" aria-controls="flush-collapseCOA">
                            What is a COA?
                        </button>
                    </h2>
                    <div id="flush-collapseCOA" class="accordion-collapse collapse" aria-labelledby="flush-headingCOA">
                        <div class="accordion-body">
                            <p class="lead">
                                A Certificate of Authenticity (CoA) is a document that accompanies a Cel when it has been released into the Collector’s Market. Cel setups sold through the 1990 Sotheby’s auction were issued their own COA for that event. Cel’s sold through Disney’s Art Program were issued a CoA in one of several designs, depending on the merchant and time of release from the Disney Company.
                            </p>
                            <p class="lead">
                                Additionally, some Cels released to employees were issued a letter from the Disney Company specifically to that employee, which serves at the CoA.
                            </p>
                            <p class="lead">
                                With one exception, CoAs do not typically indicate the Cel to which they belong and are generic in their design and text.
                            </p>
                            <p class="lead">
                                If a Cel comes with a CoA, it is highly recommended to keep the items together, as the CoA forms part of the piece as a collector’s item, as well as a part of its provenance. A CoA's design indicates at which point in time, and sometimes through which venue, the Cel was initially sold.
                            </p>
                            <p class="lead">
                                Within the consideration of collecting Cels, I do not consider third party CoA’s, those issued by galleries or auction houses, to be relevant outside the scope of sales history, and in the context of this site will use the term to refer exclusively to the CoA’s issued by, or in direct collaboration with, the Walt Disney Company.
                            </p>
