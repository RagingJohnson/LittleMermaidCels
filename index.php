                                </p>
                            </p>
                            <p class="lead">
                                A Cel setup bearing a seal indicates it is both:
                                <ul>
                                    <li class="lead">
                                        Authentic
                                    </li>
                                    <li class="lead">
                                        Legally released by the Disney Company into the Collectors’ Market
                                    </li>
                                </ul>
                            </p>
                            <p class="lead">
                                Not all Cels that lack seals were stolen from the studios. Cels that were sold at auction through Sotheby’s on 15th December, 1990 do not bear seals. Additionally, some Cels featured in Disney Store Catalogues show artwork that may not have seals. If you can trace the exact Cel (not one that’s merely “very close”) to an official Disney Store catalogue, that Cel may be exempt from having a seal.
